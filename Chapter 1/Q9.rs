// Constants of Different Types

const INTEGER_CONST: i32 = 100;
const FLOAT_CONST: f64 = 3.14;
const BOOLEAN_CONST: bool = true;
const STRING_CONST: &str = "Rust";

fn main() {
    println!("Integer: {}", INTEGER_CONST);
    println!("Float: {}", FLOAT_CONST);
    println!("Boolean: {}", BOOLEAN_CONST);
    println!("String: {}", STRING_CONST);
}
